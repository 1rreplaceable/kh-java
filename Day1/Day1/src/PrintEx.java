
public class PrintEx {

	public static void main(String[] args) {
		// 계산 (산술연산자)
		// syso + ctrl + space
		// System.out.println();
		System.out.println("1 + 1 = " + (1 + 1));
		System.out.println(2 - 2);
		
		System.out.println(10 * 10);
		System.out.println(10 / 5);
		System.out.println(10 % 2);
		System.out.println();
		
		System.out.println("프로그램 끝!");
		
		// 1) 월급 150만원 연봉을 출력?
		System.out.println("월급 150만원의 연봉: " + (150 * 12) + "만원");
		// 2) 연봉을 출력하는데 세급 10% 제외하고 연봉을 다시 출력!
		System.out.println("세금을 제외한 월급 150만원의 연봉: " + (150 * 12 * 9 / 10) + "만원");
		// 3) 시험점수가 30, 50, 40점 평균을 구해서 출력!
		System.out.println("시험점수 평균: " + ((30 + 50 + 40) / 3) + "점");
		// 4) 100초를 1분 40초로 출력
		System.out.println("100초: " + (100 / 60) + "분 " + (100 % 60) + "초");
		// println()
		System.out.println("Hello World!");
		// 1 + 2
		System.out.println(1 + 2);
		// 3 - 2
		System.out.println(3 - 2);
		
		// 7 3 * / 몫, 나머지 값
		System.out.println(7 * 3);
		System.out.println(7 / 3);
		System.out.println(7 % 3);
	}

}
