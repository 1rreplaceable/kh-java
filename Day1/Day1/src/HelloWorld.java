
public class HelloWorld {

	public static void main(String[] args) {
		// 프로그램이 시작하면 가장 먼저실행되는 함수 main
		
		// 컴파일: 번역가
		// 	     번역은 한번에 실행되고 결과까지 보여준다.
		
		// 명령어:
		// 프로그램에서 결과를 확인할 때 사용하는 기능(함수) 출력
		// println() 괄호 안에 값을 넣는다.
		// 실행 Ctrl + F11
		
		// 정수: 소수점이 없는 숫자 (int)
		System.out.println(1);
		System.out.println(-2);
		System.out.println(1000);
		
		// 실수: 소수점이 있는 숫자 (double)
		System.out.println(1.1);
		System.out.println(3.145516);
		
		// 문자
		System.out.println('A');
		System.out.println('*');
		System.out.println('ㄱ');
		
		// 문자열
		System.out.println(" 안녕하세요!");
		System.out.println("오늘은 첫날!");
	}

}
